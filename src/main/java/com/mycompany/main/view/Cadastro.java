private void atualizaTabela() throws SQLException {
    DefaultTableModel modelo = (DefaultTableModel) tableLivros2.getModel();
    modelo.setNumRows(0); // limpa a tabela

    try {
        ArrayList<Livro> livros = livroDAO.retornaListaDeLivros();
        
        for (Livro l : livros){
            modelo.addRow(new Object[]{
                l.getId(),
                l.getNomeDoLivro(),
                l.getAno(),
                l.getEditora(),
                l.isDisponivel()
            });
        }
    } catch (SQLException e) {
        System.err.println("Erro ao atualizar tabela: " + e.getMessage());
    }
}
