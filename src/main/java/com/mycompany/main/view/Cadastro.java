    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            Livro l = new Livro();
            l.setNomeDoLivro(txtNome.getText());      // aqui é txtNome
            l.setAno(Integer.parseInt(txtAno.getText()));
            l.setEditora(txtEditora.getText());

            // Verifica qual rádio está selecionado
            if (rdbSim.isSelected()) {
                l.setDisponivel(true);
            } else if (rdbNao.isSelected()) {
                l.setDisponivel(false);
            } else {
                System.out.println("Selecione se o livro está disponível.");
                return;
            }

            livroDAO.salvar(l); // método salvar no DAO

            // Atualiza tabela
            atualizaTabela();

            // Limpa os campos
            txtNome.setText("");        // limpa txtNome
            txtAno.setText("");
            txtEditora.setText("");
            rdbSim.setSelected(false);
            rdbNao.setSelected(false);

            System.out.println("Livro salvo com sucesso!");
        } catch (SQLException e) {
            System.out.println("Erro ao salvar livro: " + e.getMessage());
        } catch (NumberFormatException e) {
            System.out.println("Erro: ano inválido. " + e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

